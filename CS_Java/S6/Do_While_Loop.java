class Do_While_Loop
{
	public static void main(String[] args)
	{
		int i = 0;
		do	
		{
			System.out.println(i);
			i++;
		}
		while(i<=10);
	}
}

/*
 POINTS TO REMEMBER:
					1. semi colon is required after the while condition 
					2. Even if the condition is false for the very first time your statements inside the code block will get executed
*/
