class Switch_Case_1    // Switch case for Character 
{
	public static void main(String[] args)
	{
		// char s = 'a';
	     //  char s = 'z';  
		   char s = 'B';  // Cahracters and Strings are case sensitive
		switch (s)
		{
				case 'a': 
						System.out.println("The value is a");
						break;
				case 'b': 
						System.out.println("The value is b");
						break;
				case 'c': 
						System.out.println("The value is c");
						break;
				default :
						System.out.println("The value of s is any otheer ");
						break;
		}
	}
	
}