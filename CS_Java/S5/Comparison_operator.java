class Comparison_operator
{
	public static void main(String[] args)
	{
		int x = 5;

		
		if (x == 5)
		{
			System.out.println("Value of x is 5");
		}
		else
		{
			System.out.println("Value of x is not 5");
		}
		

		int y = 7;

		if(y != 7)
		{
			System.out.println("Value is not equals to 7");
		}
		else
		{
			System.out.println("Value is 7");
		}
	}
}