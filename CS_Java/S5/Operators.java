class Operators
{
	public static void main(String[] args)
	{
		
		int x = 7;  
		System.out.println("Value of x is "+x);

		int y = 7 + 5;
		System.out.println("Value of y is "+y);

		String s = "Hello" + "World";
		System.out.println("Value of String s is "+s);

		int p = 5;
		int q = 7-p;
		System.out.println("Value of q is "+q);

		int r = 12;
		int s1 = 12*3;
		System.out.println("Value of s1 is "+s1);

		//int a1 = 12;
		int a2 = 24/2;
		System.out.println("Value of a2 is "+a2);

		int mod = 13%2;   // Modulus operator in java is percentile i.e is %
		System.out.println("Mod value is "+mod);

		int i = 5;  //Increment operator
		System.out.println("Value of i is "+i++);
		System.out.println("Value of i is "+i);

		int j = 5;
		System.out.println("Value of j is "+ --j);
		System.out.println("Value of j is "+ j);		



	}
}